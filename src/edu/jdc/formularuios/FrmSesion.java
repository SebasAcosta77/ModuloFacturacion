    private void btnAccesderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccesderActionPerformed
        String correo, clave;
        
        correo = cajaCorreo.getText();
        clave = new String (cajaClave.getPassword());
        
        if (correo.isEmpty() || clave.isEmpty()){
            JOptionPane.showMessageDialog(PanelPrincipal, "faltan datos para el inicio de sesion", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }else{
            Acceso objAcceso = new Acceso();
            objAcceso.setCorreoAcceso(correo);
            objAcceso.setClaveUsuario(clave);
            
            objUsuario = objMiDao.iniciosesion(objAcceso);
            
            if (objUsuario != null){
                FrmPrincipa miVentana = new FrmPrincipa(objUsuario);
                miVentana.setVisible(true);
                this.dispose();
                
            }else{
                JOptionPane.showMessageDialog(PanelPrincipal, "fallo obtener credenciales", "error", JOptionPane.ERROR_MESSAGE);
                
            }
            
            
        }
    }//GEN-LAST:event_btnAccesderActionPerformed

